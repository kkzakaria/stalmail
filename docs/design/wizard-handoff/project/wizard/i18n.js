window.wizT = function (lang, key, vars) {
  var dict = window.WIZ_I18N[lang] || window.WIZ_I18N.fr;
  var s = dict[key];
  if (s === undefined) s = window.WIZ_I18N.fr[key];
  if (s === undefined) s = key;
  if (vars) {
    for (var k in vars) s = s.split("{" + k + "}").join(vars[k]);
  }
  return s;
};

